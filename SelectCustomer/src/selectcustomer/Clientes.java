        public void escribirEnTXT() {
        String[] encabezados = {"Nombre", "Apellidos", "Domicilio", "Tel√©fono", "Email"};
        String[] registros = new String[5];
        DefaultTableModel model = new DefaultTableModel(null, encabezados);

        String cadSQL = "SELECT * FROM clientes";

        try {
            Statement st = con.createStatement();
            ResultSet rset = st.executeQuery(cadSQL);
            while (rset.next()) {
                registros[0] = rset.getString("Nombre");
                registros[1] = rset.getString("Apellidos");
                registros[2] = rset.getString("Domicilio");
                registros[3] = rset.getString("Telefono");
                registros[4] = rset.getString("Email");
                model.addRow(registros);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al obtener los datos de la tabla clientes: " + e.getMessage());
        }

        String rutaProyecto = "D:\\Kyelek\\Documentos\\NetBeansProjects\\SelectCustomer"; // Establecer la ruta manualmente
        String rutaCarpetaArchivos = rutaProyecto + File.separator + "Clientes";
        File carpetaArchivos = new File(rutaCarpetaArchivos);
        //System.out.println(rutaCarpetaArchivos);
        
        if (!carpetaArchivos.exists()) {
            carpetaArchivos.mkdirs();
        }

        int contador = 1;
        File archivo = new File(carpetaArchivos, "cliente" + contador + ".html");
        while (archivo.exists()) {
            contador++;
            archivo = new File(carpetaArchivos, "cliente" + contador + ".html");
        }

        try (FileWriter writer = new FileWriter(archivo)) {
            writer.write("<h1>Cliente</h1>");
            for (int i = 0; i < model.getRowCount(); i++) {
                for (int j = 0; j < model.getColumnCount(); j++) {
                    writer.write(model.getValueAt(i, j).toString());
                    if (j != model.getColumnCount() - 1) {
                        writer.write("\t");
                    }
                }
                writer.write("\n");
            }
            JOptionPane.showMessageDialog(null, "Archivo creado correctamente en la carpeta \"archivos\".");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Error al crear el archivo de texto: " + e.getMessage());
        }
    }

